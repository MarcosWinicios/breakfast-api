package com.marcos.breakfast.api.model;

public class EmployeeModel {
	private Long id;
	private String name;
	
	public Long getId() {
		return id;
	}
	public void setId(Long idEmployee) {
		this.id = idEmployee;
	}
	public String getName() {
		return name;
	}
	public void setName(String nameEmployee) {
		this.name = nameEmployee;
	}
}
